export function findProduct(slug) {
  return products.find(p => p.slug === slug || String(p.id) === String(slug))
}

export function relatedProducts(slug, limit = 3) {
  const current = findProduct(slug)
  if (!current) return products.slice(0, limit)
  return products
    .filter(p => p.slug !== slug && p.cat === current.cat)
    .concat(products.filter(p => p.slug !== slug && p.cat !== current.cat))
    .slice(0, limit)
}
